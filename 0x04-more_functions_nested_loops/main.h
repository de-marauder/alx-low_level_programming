#ifndef MAIN_H
#define MAIN_H

/**
 * _isdigit - check if input is a digit
 * @i: character to check
 * Return: 1 if true, 0 if false
 */

int _isdigit(int i);


/**
 * mul - multiplies to numbers
 * @a: number
 * @b: number
 * Return: product of @a and @b
 */
int mul(int a, int b);

/**
 * _isupper - checks for uppercase character
 * @c: character to check
 * Return: 1 if true, 0 if false
 */

int _isupper(int c);


#endif
